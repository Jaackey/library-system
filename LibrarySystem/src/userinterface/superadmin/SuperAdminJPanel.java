    private void viewDetailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = libraryTable.getSelectedRow();
        if(selectedRow >= 0){
            Library library = (Library)libraryTable.getValueAt(selectedRow, 0);
            ViewLibDetailJPanel vldjp = new ViewLibDetailJPanel(userProcessContainer, system, library);
            userProcessContainer.add("ViewLibDetailJPanel", vldjp);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else{
            JOptionPane.showMessageDialog(null, "No Library Selected");
            return;
        }
        
    }//GEN-LAST:event_viewDetailBtnActionPerformed

