    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) libraryTable.getModel();       
        model.setRowCount(0);
        for(Library lib : system.getLibraryList()){
            Object[] row = new Object[1];
            row[0] = lib;
            model.addRow(row);
        }
    }

