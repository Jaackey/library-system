    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) purchaseTable.getModel();       
        model.setRowCount(0);
        for(WorkRequest wr : library.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[7];
            row[0] = wr.getBook();
            row[1] = wr.getBook().getAuthor();
            row[2] = wr.getBook().getPress();
            row[3] = wr.getBook().getDate();
            row[4] = wr.getBook().getInprice();
            row[5] = wr.getRequestDate();
            row[6] = wr;
            model.addRow(row);
        }
    }
    
    public void addPurchaseToAccount(Book book, int num){
        AccountItem ai = library.getLibraryAccount().createAccountItem(AccountItem.Type.Spent);
        double total = book.getInprice() * num;
        ai.setMoney(total);
        ai.setDate(new Date());
        ai.setExplain(book.getName());
        ai.setReceiver(String.valueOf(num));
    }

