    private void purchaseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_purchaseBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = purchaseTable.getSelectedRow();
        if(selectedRow >= 0){
            int buyNum = (Integer)amountSpinner.getValue();
            if(buyNum == 0){
                JOptionPane.showMessageDialog(null, "Please Purchase More Than Zero!");
                return;
            }
            else{
                Book book = (Book)purchaseTable.getValueAt(selectedRow, 0);
                book.setAmount(buyNum);
                book.setLocation("A00-000");
                library.getBookCatalog().getBookList().add(book);
                addPurchaseToAccount(book, buyNum);
                WorkRequest wr = (WorkRequest)purchaseTable.getValueAt(selectedRow, 6);
                wr.setStatus("Processed");
                populateTable();
                JOptionPane.showMessageDialog(null, "Success!");
                PurchaseNewBookRunnable pnbr = new PurchaseNewBookRunnable(library, book);
                Thread t1 = new Thread(pnbr);
                t1.start();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "No Book Selected");
            return;
        }
    }//GEN-LAST:event_purchaseBtnActionPerformed


