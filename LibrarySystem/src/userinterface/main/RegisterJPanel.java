    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        if(!checkFilled()){
            JOptionPane.showMessageDialog(null, "Please Fill in Every Blank!");
            return;
        }
        if(!CheckUnique.checkUniaue(system, userText.getText())){
            JOptionPane.showMessageDialog(null, "User Name Already Existed!");
            return;
        }
        if(!checkAge(ageText.getText())){
            JOptionPane.showMessageDialog(null, "Please Input Correct Age!");
            return;
        }
        Person person = new Person();
        person.setName(nameText.getText());
        person.setAge(Integer.parseInt(ageText.getText()));
        person.setAddress(addressText.getText());
        person.setZipCode(zipText.getText());
        person.setTelNumber(telText.getText());
        Organization org = system.getOrganizationCatalog().getOrganizationList().get(0);
        Role role = org.getSupportedRole().get(0);
        org.getUserAccountCatalog().createUserAccount(userText.getText(), passwordText.getText(), person, role);
        JOptionPane.showMessageDialog(null, "Success! Please Login Now!");
        container.removeAll();
        JPanel blankJP = new JPanel();
        container.add("blank", blankJP);
        CardLayout crdLyt = (CardLayout) container.getLayout();
        crdLyt.next(container);
    }//GEN-LAST:event_createBtnActionPerformed


