    public boolean checkFilled(){
        if(nameText.getText().trim().equals("") || 
                ageText.getText().trim().equals("") || 
                addressText.getText().trim().equals("") || 
                zipText.getText().trim().equals("") || 
                telText.getText().trim().equals("") || 
                userText.getText().trim().equals("") || 
                passwordText.getText().trim().equals("")){
            return false;
        }
        else{
            return true;
        }
    }
    
    public boolean checkAge(String age){
        Pattern pattern = Pattern.compile("^[1-9]\\d?\\d?$");
        Matcher matcher = pattern.matcher(age);
        return matcher.matches();
    }
    
